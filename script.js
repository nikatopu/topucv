// Function to create content blocks based on the list
function createContentBlocks(list) {
  const container = document.getElementById("content-container");
  container.innerHTML = ""; // Clear previous content

  // Loop through the list and create content blocks
  list.forEach((item, index) => {
    const block = document.createElement("div");
    block.classList.add("content-block");
    if (index % 2 === 0) {
      block.classList.add("a-block");
    } else {
      block.classList.add("b-block");
    }

    const textDiv = document.createElement("div");
    textDiv.classList.add("block-text");
    textDiv.innerHTML = `<h2>${item.title}</h2><p>${item.text}</p>`;

    const imageDiv = document.createElement("div");
    imageDiv.classList.add("block-image");
    const image = document.createElement("img");
    image.src = item.image;
    image.alt = `${item.title} Image`;
    image.style = "cursor: pointer;";
    image.addEventListener("click", () => {
      window.open(item.link);
    });

    if (index % 2 === 0) {
      block.appendChild(imageDiv);
    }

    imageDiv.appendChild(image);
    block.appendChild(textDiv);
    if (index % 2 === 1) {
      block.appendChild(imageDiv);
    }
    container.appendChild(block);
  });
}

// Call the function to create content blocks
createContentBlocks(work_experience);

// Button event listeners to switch between states
const all_states = [
  {
    state: document.getElementById("state1Btn"),
    data: work_experience,
  },
  {
    state: document.getElementById("state2Btn"),
    data: game_development,
  },
  {
    state: document.getElementById("state3Btn"),
    data: creative_writing,
  },
];

all_states[0].state.classList.add("active");

// I know this is a nested loop but... come on
for (let i = 0; i < all_states.length; i++) {
  all_states[i].state.addEventListener("click", () => {
    createContentBlocks(all_states[i].data);
    for (let j = 0; j < all_states.length; j++) {
      if (j === i) {
        all_states[j].state.classList.add("active");
      } else {
        all_states[j].state.classList.remove("active");
      }
    }
  });
}
